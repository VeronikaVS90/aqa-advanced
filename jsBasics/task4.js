// a circle area
let radiusCircle = 10;
let areaCircle = Math.PI * Math.pow(radiusCircle, 2);
console.log(areaCircle.toFixed(2));

// a rectangle area
let length = 9;
let width = 3;
let areaRectangle = length * width;
console.log(areaRectangle.toFixed(2));

// a cylinder volume
let radiusCylinder = 5;
let height = 9;
let volumeCylinder = Math.PI * Math.pow(radiusCylinder, 2) * height;
console.log(volumeCylinder.toFixed(2));
